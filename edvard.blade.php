    <p>Pintor excéntrico e innovador de origen noruego. Durante su carrera persiguió plasmar las caras de la psicología humana, representando "el alma" en sus propias palabras.

        A los 18 años abandonó la carrera de ingeniería donde en el primer año, había aprendido los conceptos básicos del dibujo técnico. Entonces su gran ilusión era convertirse en pintor a tiempo completo, hecho que detonó toda clase de cuestionamientos en su entorno familiar y social
        
        Se considera como uno de los grandes del expresionismo y precursor del modernismo. A pesar de que sus trabajos no demuestran una pericia técnica despampanante, se desenvuelve como pez en el agua con el simbolismo y lo conceptual.</p>
        <p>Las pinturas y grabados de Edvard Munch recorren una línea temática muy marcada, con frecuencia relacionadas con la depresión y una mirada extremadamente negativa de la existencia. Con todo ello es considerado un genio, por su propuesta estética y su personalidad.</p>
